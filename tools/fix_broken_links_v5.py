def main():
    files_touched = set()
    pdf_json_fixed = 0
    quota_fixed = 0

    for src in VAULT.rglob('*.md'):
        if any(part.startswith('.') for part in src.parts):
            continue
        text = src.read_text(encoding='utf-8', errors='ignore')
        original = text

        # 1. 路径映射
        for old, new in PATH_FIXES.items():
            for sep in [f'[[{old}]]', f'[[{old}|']:
                if sep in text:
                    text = text.replace(sep, f'[[{new}|' if sep.endswith('|') else f'[[{new}]]')

        # 2. 配额册短名映射
        for short, full in QUOTA_FILES.items():
            for sep in [f'[[{short}]]', f'[[{short}|']:
                if sep in text:
                    text = text.replace(sep, f'[[{full}|' if sep.endswith('|') else f'[[{full}]]')
                    quota_fixed += 1

        # 3. PDF 不存在转外链
        pdf_pattern = re.compile(r'\[\[([^\]\n]+\.pdf)\]\]')
        for m in list(pdf_pattern.finditer(text)):
            pdf_target = m.group(1).replace('\\', '/')
            if not (VAULT / pdf_target).exists():
                alt_text = pdf_target.split('/')[-1].replace('.pdf', '')
                replacement = f'[{alt_text}](file:///{pdf_target})'
                text = text.replace(f'[[{m.group(1)}]]', replacement)
                pdf_json_fixed += 1

        # 4. JSON 不存在转外链
        json_pattern = re.compile(r'\[\[([^\]\n]+\.json)\]\]')
        for m in list(json_pattern.finditer(text)):
            json_target = m.group(1).replace('\\', '/')
            if not (VAULT / json_target).exists():
                alt_text = json_target.split('/')[-1].replace('.json', '')
                replacement = f'[{alt_text}](file:///{json_target})'
                text = text.replace(f'[[{m.group(1)}]]', replacement)
                pdf_json_fixed += 1

        # 5. .py 不存在转外链
        py_pattern = re.compile(r'\[\[([^\]\n]+\.py)\]\]')
        for m in list(py_pattern.finditer(text)):
            py_target = m.group(1).replace('\\', '/')
            if not (VAULT / py_target).exists():
                alt_text = py_target.split('/')[-1].replace('.py', '')
                replacement = f'[{alt_text}](file:///{py_target})'
                text = text.replace(f'[[{m.group(1)}]]', replacement)
                pdf_json_fixed += 1

        # 6. daily_crawl.py 短名
        for sep in ['[[daily_crawl.py]]', '[[daily_crawl.py|']:
            if sep in text:
                text = text.replace(sep, '[[tools/crawl_xj_info_price.py|daily_crawl.py]]' if sep.endswith('|') else '[[tools/crawl_xj_info_price.py]]')

        if text != original:
            src.write_text(text, encoding='utf-8')
            files_touched.add(src)

    print(f"已修改文件: {len(files_touched)}")
    print(f"PDF/JSON/PY 外链: {pdf_json_fixed}")
    print(f"配额册映射: {quota_fixed}")

if __name__ == '__main__':
    main()