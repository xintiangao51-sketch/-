def scf(cell, text, sz=9, bold=False, align=WD_ALIGN_PARAGRAPH.CENTER, fn='宋体', color=None):
    """Set cell font"""
    cell.text = ''
    p = cell.paragraphs[0]
    p.alignment = align
    p.paragraph_format.space_before = Pt(1)
    p.paragraph_format.space_after = Pt(1)
    run = p.add_run(str(text))
    run.font.name = fn
    run.font.size = Pt(sz)
    run.font.bold = bold
    run._element.rPr.rFonts.set(qn('w:eastAsia'), fn)
    if color:
        run.font.color.rgb = color

def shade_row(row, color='D6E4F0'):
    for cell in row.cells:
        shading = parse_xml(f'<w:shd {nsdecls("w")} w:fill="{color}"/>')
        cell._element.get_or_add_tcPr().append(shading)

def shade_cells(row, color='2F5496'):
    for cell in row.cells:
        shading = parse_xml(f'<w:shd {nsdecls("w")} w:fill="{color}"/>')
        cell._element.get_or_add_tcPr().append(shading)

def make_table(doc, headers, data, col_widths=None):
    """Create formatted table"""
    nrows = 1 + len(data)
    ncols = len(headers)
    table = doc.add_table(rows=nrows, cols=ncols)
    table.style = 'Table Grid'
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, h in enumerate(headers):
        scf(table.rows[0].cells[i], h, sz=9, bold=True, color=RGBColor(255,255,255))
    shade_cells(table.rows[0], '2F5496')
    for r, row_data in enumerate(data):
        for c, val in enumerate(row_data):
            scf(table.rows[r+1].cells[c], val, sz=9)
            if r % 2 == 1:
                shade_row(table.rows[r+1], 'D6E4F0')
    if col_widths:
        for i, w in enumerate(col_widths):
            for row in table.rows:
                row.cells[i].width = Cm(w)
    doc.add_paragraph()  # spacer
    return table

