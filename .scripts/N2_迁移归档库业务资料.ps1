# 执行迁移
$totalMoved = 0
$totalSkipped = 0
$totalDeleted = 0
$log = @()

foreach ($subDir in $mapping.Keys) {
    $srcDir = Join-Path $srcBase $subDir
    if (-not (Test-Path $srcDir)) {
        continue
    }

    foreach ($entry in $mapping[$subDir].GetEnumerator()) {
        $fileName = $entry.Key
        $target = $entry.Value

        $srcFile = Join-Path $srcDir $fileName
        if (-not (Test-Path $srcFile)) {
            continue
        }

        # 处理删除项
        if ($target -eq "_DELETE_") {
            Remove-Item $srcFile -Force
            $totalDeleted++
            $log += "🗑️  [删除] $subDir/$fileName"
            continue
        }

        # 处理移动项
        $dstFile = Join-Path $dstBase $target
        $dstPath = Split-Path $dstFile -Parent

        if (-not (Test-Path $dstPath)) {
            New-Item -Path $dstPath -ItemType Directory -Force | Out-Null
        }

        # 跳过已存在
        if (Test-Path $dstFile) {
            $totalSkipped++
            $log += "⏭️  [跳过] $subDir/$fileName → $target (已存在)"
            continue
        }

        Move-Item $srcFile $dstFile -Force
        $totalMoved++
        $log += "📦  [移动] $subDir/$fileName → $target"
    }
}

# 输出日志
Write-Host ""
Write-Host "===== 迁移完成 =====" -ForegroundColor Cyan
Write-Host "✅ 移动：$totalMoved 个文件" -ForegroundColor Green
Write-Host "⏭️  跳过：$totalSkipped 个文件 (已存在)" -ForegroundColor Yellow
Write-Host "🗑️  删除：$totalDeleted 个文件" -ForegroundColor Red
Write-Host ""
Write-Host "===== 详细日志 =====" -ForegroundColor Cyan
$log | ForEach-Object { Write-Host $_ }
Write-Host ""

# 保存日志
